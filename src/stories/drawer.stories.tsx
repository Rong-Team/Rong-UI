import React, { useState } from 'react'
import { Drawer, Button } from '../../components'
import { themeIt } from './utils/withTheme'


export default {
    title: 'Data Display / Drawer',
    component: Drawer as any,
    decorators: [themeIt],
    parameters: {
        docs: {
            description: 'A Drawer is a panel that is typically overlaid on top of a page and slides in from the side. '+
            'It contains a set of information or actions. Since the user can interact with the Drawer without leaving the current page, '+
           'tasks can be achieved more efficiently within the same context. '
        }
    },
    argTypes: {

    },

};

export const Basic=()=>{
    const [visible, setVisible] = useState(false);
  const showDrawer = () => {
    setVisible(true);
  };
  const onClose = () => {
    setVisible(false);
  };
  return (
    <>
      <Button type="primary" onClick={showDrawer}>
        Open
      </Button>
      <Drawer
        title="Basic Drawer"
        placement="right"
        closable={false}
        onClose={onClose}
        visible={visible}
      >
        <p>Some contents...</p>
        <p>Some contents...</p>
        <p>Some contents...</p>
      </Drawer>
    </>
  );
}

